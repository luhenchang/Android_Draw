class GriendLineayoutTestView @JvmOverloads constructor(context: Context, attrs: AttributeSet? = null, defStyle: Int = 0) : androidx.appcompat.widget.AppCompatImageView(context, attrs, defStyle) {
    init {

    }
    val paint=Paint()
    override fun onSizeChanged(w: Int, h: Int, oldw: Int, oldh: Int) {
        super.onSizeChanged(w, h, oldw, oldh)
        paint.shader=LinearGradient(0f,0f,width.toFloat(),0f, Color.YELLOW,Color.RED,Shader.TileMode.CLAMP)

    }
    override fun onDraw(canvas: Canvas) {
        super.onDraw(canvas)
        paint.strokeWidth=5f
        paint.style= Paint.Style.STROKE
        canvas.translate(0f,height/2f)
        canvas.scale(1f,-1f)
        canvas.drawCircle(0f,0f,100f,paint)
        for (index in 0 until 20){
            val random= 40f
            val path=Path()
            path.moveTo(index*20f,-random)
            path.lineTo(index*20f,random)
            canvas.drawPath(path,paint)
        }
    }

}